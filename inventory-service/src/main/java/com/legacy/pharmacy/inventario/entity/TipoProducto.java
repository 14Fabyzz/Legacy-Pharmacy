package com.legacy.pharmacy.inventario.entity;

/**
 * Enum para clasificar productos según su naturaleza.
 * - TANGIBLE: Productos físicos que manejan stock (medicamentos, insumos)
 * - SERVICIO: Productos intangibles sin stock (inyectología, consultas)
 */
public enum TipoProducto {
    TANGIBLE,
    SERVICIO
}
